#Sofia Ă© revisora de textos e precisa identificar palavras muito longas em um parĂ¡grafo. Textos mais fĂ¡ceis de ler costumam ter palavras curtas, entĂ£o ela quer um programa que encontre palavras que tenham mais de 10 letras e as exiba em destaque. Crie um programa que receba um texto e exiba todas as palavras que possuem mais de 10 letras. Caso nenhuma palavra longa seja encontrada, o programa deve avisar o usuĂ¡rio.

def encontrar_palavras_longas(texto):
    if not isinstance (texto, str):
        raise TypeError ("Por gentileza, utilizar palavras.")
    
    palavras = texto.strip().split()
    palavras_longas = []

    for palavra in palavras:
        if len(palavra) > 10:
            palavras_longas.append(palavra)

    return palavras_longas

texto = input("Digite um texto: ")

resultado = encontrar_palavras_longas(texto)

